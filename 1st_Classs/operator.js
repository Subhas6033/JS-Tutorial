/* 
Arithmetic Operators :: ==>
Arithmetic operators are used to perform mathematical calculations on numbers. The most common arithmetic operators in JavaScript are::

1. Addition (➕): Adds two numbers together.
2. Subtraction (➖): Subtracts one number from another.
3. Multiplication (*): Multiplies two numbers together.
4. Division (/): Divides one number by another.
5. Modulus (%): Returns the remainder of a division operation.
6. Exponentiation (**): Raises a number to the power of another number.
7. Increment (➕➕) and Decrement (➖➖): Increases or decreases a number by one, respectively.
*/

// 1. Addition (+) :: ==> Adds two numbers together.
let a = 10; 
let b = 5;
let sum = a + b; // 10 + 5 = 15
console.log("Addition:", sum); // Output :: Addition: 15

// 2. Subtraction (-) :: ==> Subtracts one number from another.
let difference = a - b; // 10 - 5 = 5
console.log("Subtraction:", difference); // Output :: Subtraction: 5


// 3. Multiplication (*) :: ==> Multiplies two numbers together.
let product = a * b; // 10 * 5 = 50
console.log("Multiplication:", product); // Output :: Multiplication: 50

// 4. Division (/) :: ==> Divides one number by another.
let quotient = a / b; // 10 / 5 = 2
console.log("Division:", quotient); // Output :: Division: 2


// 5. Modulus (%) :: ==> Returns the remainder of a division operation.
let remainder = a % b; // 10 % 5 = 0
console.log("Modulus:", remainder); // Output :: Modulus: 0
